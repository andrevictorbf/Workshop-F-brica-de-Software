#Cálculo de número antecessor e sucessor - Há uso dos comandos input - entrada da variável - print - para a impresão na tela do usuário - conjunto de números inteiros - int;
# O "F" serve para concatenar as variáveis. 
num = input ("Digite uma variável ")

antecessor = int(num) - 1
sucessor = int(num) + 1

print (f"O numero digitado foi {num} e seu antecessor é {antecessor} e o seu sucessor é {sucessor}")
